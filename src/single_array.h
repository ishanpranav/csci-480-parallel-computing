/** Represents an array of single-precision floating-point numbers. */
typedef float *SingleArray;

/**
 * Initializes a new array of single-precision floating-point numbers with the
 * specified `length`.
 * 
 * @param length the size of the array to create.
 * @return A new one-dimensional array of single-precision floating-point
 *         numbers with the specified length.
*/
SingleArray single_array(int length);
